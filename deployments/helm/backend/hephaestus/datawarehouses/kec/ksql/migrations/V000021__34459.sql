    STUDENT_ENROLLMENT_STATUS_HISTORY_STREAM_FORMATED_V1.UPDATED_AT AS UPDATED_AT,
    STUDENT_ENROLLMENT_STATUS_HISTORY_STREAM_FORMATED_V1.DELETED_AT AS DELETED_AT,
