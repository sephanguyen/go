        STUDENT_ENROLLMENT_STATUS_HISTORY_STREAM_ORIGIN_V1.AFTER->STUDENT_ID AS STUDENT_ID,
        STUDENT_ENROLLMENT_STATUS_HISTORY_STREAM_ORIGIN_V1.AFTER->LOCATION_ID AS LOCATION_ID,
