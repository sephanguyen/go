        STUDENT_ENROLLMENT_STATUS_HISTORY_STREAM_ORIGIN_V1.AFTER->UPDATED_AT AS UPDATED_AT,
        STUDENT_ENROLLMENT_STATUS_HISTORY_STREAM_ORIGIN_V1.AFTER->DELETED_AT AS DELETED_AT,
        STUDENT_ENROLLMENT_STATUS_HISTORY_STREAM_ORIGIN_V1.AFTER->ORDER_ID AS ORDER_ID,
        STUDENT_ENROLLMENT_STATUS_HISTORY_STREAM_ORIGIN_V1.AFTER->ORDER_SEQUENCE_NUMBER AS ORDER_SEQUENCE_NUMBER

    FROM STUDENT_ENROLLMENT_STATUS_HISTORY_STREAM_ORIGIN_V1
