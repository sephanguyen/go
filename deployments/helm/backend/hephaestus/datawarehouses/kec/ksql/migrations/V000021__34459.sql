    STUDENT_ENROLLMENT_STATUS_HISTORY_STREAM_FORMATED_V1.ENROLLMENT_STATUS AS ENROLLMENT_STATUS,
    STUDENT_ENROLLMENT_STATUS_HISTORY_STREAM_FORMATED_V1.START_DATE AS START_DATE,
    STUDENT_ENROLLMENT_STATUS_HISTORY_STREAM_FORMATED_V1.END_DATE AS END_DATE,
    STUDENT_ENROLLMENT_STATUS_HISTORY_STREAM_FORMATED_V1.COMMENT AS COMMENT,
