    SCHOOL_LEVEL_GRADE_PUBLIC_INFO_V1.SCHOOL_LEVEL_GRADE_UPDATED_AT AS SCHOOL_LEVEL_GRADE_UPDATED_AT,
    SCHOOL_LEVEL_GRADE_PUBLIC_INFO_V1.SCHOOL_LEVEL_GRADE_DELETED_AT AS SCHOOL_LEVEL_GRADE_DELETED_AT

FROM SCHOOL_LEVEL_GRADE_PUBLIC_INFO_V1 
JOIN SCHOOL_LEVEL_TABLE_FORMATED_V1 ON SCHOOL_LEVEL_TABLE_FORMATED_V1.ID = SCHOOL_LEVEL_GRADE_PUBLIC_INFO_V1.SCHOOL_LEVEL_ID;


