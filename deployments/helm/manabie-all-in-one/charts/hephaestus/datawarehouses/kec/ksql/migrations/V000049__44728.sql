SET 'auto.offset.reset' = 'earliest';

DROP STREAM IF EXISTS SCHOOL_LEVEL_PUBLIC_INFO_V1 DELETE TOPIC;
DROP STREAM IF EXISTS SCHOOL_LEVEL_STREAM_FORMATED_V1 DELETE TOPIC;

DROP STREAM IF EXISTS GRADE_STREAM_FORMATED_V1 DELETE TOPIC;

DROP STREAM IF EXISTS SCHOOL_LEVEL_GRADE_STREAM_FORMATED_V1 DELETE TOPIC;


CREATE STREAM IF NOT EXISTS SCHOOL_LEVEL_STREAM_FORMATED_V1
    AS SELECT
        SCHOOL_LEVEL_STREAM_ORIGIN_V1.AFTER->SCHOOL_LEVEL_ID AS KEY,
        AS_VALUE(SCHOOL_LEVEL_STREAM_ORIGIN_V1.AFTER->SCHOOL_LEVEL_ID) AS SCHOOL_LEVEL_ID,
        SCHOOL_LEVEL_STREAM_ORIGIN_V1.AFTER->SCHOOL_LEVEL_NAME AS SCHOOL_LEVEL_NAME,
        SCHOOL_LEVEL_STREAM_ORIGIN_V1.AFTER->IS_ARCHIVED AS IS_ARCHIVED,
        SCHOOL_LEVEL_STREAM_ORIGIN_V1.AFTER->CREATED_AT AS CREATED_AT,
        SCHOOL_LEVEL_STREAM_ORIGIN_V1.AFTER->UPDATED_AT AS UPDATED_AT,
        SCHOOL_LEVEL_STREAM_ORIGIN_V1.AFTER->DELETED_AT AS DELETED_AT,
        SCHOOL_LEVEL_STREAM_ORIGIN_V1.AFTER->SEQUENCE AS SEQUENCE,
        '' AS GRADE_ID

    FROM SCHOOL_LEVEL_STREAM_ORIGIN_V1
    WHERE SCHOOL_LEVEL_STREAM_ORIGIN_V1.AFTER->RESOURCE_PATH = '{{ .Values.kecResourcePath }}'
    PARTITION BY AFTER->SCHOOL_LEVEL_ID
    EMIT CHANGES;
CREATE TABLE IF NOT EXISTS SCHOOL_LEVEL_TABLE_FORMATED_V1 (ID VARCHAR PRIMARY KEY) with (kafka_topic='{{ .Values.topicPrefix }}SCHOOL_LEVEL_STREAM_FORMATED_V1', value_format='AVRO');


CREATE STREAM IF NOT EXISTS GRADE_STREAM_FORMATED_V1
    AS SELECT
        GRADE_STREAM_ORIGIN_V1.AFTER->GRADE_ID AS KEY,
        AS_VALUE(GRADE_STREAM_ORIGIN_V1.AFTER->GRADE_ID) AS GRADE_ID,
        GRADE_STREAM_ORIGIN_V1.AFTER->NAME AS NAME,
        GRADE_STREAM_ORIGIN_V1.AFTER->IS_ARCHIVED AS IS_ARCHIVED,
        GRADE_STREAM_ORIGIN_V1.AFTER->CREATED_AT AS CREATED_AT,
        GRADE_STREAM_ORIGIN_V1.AFTER->UPDATED_AT AS UPDATED_AT,
        GRADE_STREAM_ORIGIN_V1.AFTER->DELETED_AT AS DELETED_AT,
        GRADE_STREAM_ORIGIN_V1.AFTER->PARTNER_INTERNAL_ID AS PARTNER_INTERNAL_ID,
        GRADE_STREAM_ORIGIN_V1.AFTER->SEQUENCE AS SEQUENCE

    FROM GRADE_STREAM_ORIGIN_V1
    WHERE GRADE_STREAM_ORIGIN_V1.AFTER->RESOURCE_PATH = '{{ .Values.kecResourcePath }}'
    PARTITION BY AFTER->GRADE_ID
    EMIT CHANGES;
CREATE TABLE IF NOT EXISTS GRADE_TABLE_FORMATED_V1 (ID VARCHAR PRIMARY KEY) with (kafka_topic='{{ .Values.topicPrefix }}GRADE_STREAM_FORMATED_V1', value_format='AVRO');

CREATE STREAM IF NOT EXISTS SCHOOL_LEVEL_GRADE_STREAM_FORMATED_V1
    AS SELECT
        SCHOOL_LEVEL_GRADE_STREAM_ORIGIN_V1.AFTER->SCHOOL_LEVEL_ID + SCHOOL_LEVEL_GRADE_STREAM_ORIGIN_V1.AFTER->GRADE_ID AS KEY,
        SCHOOL_LEVEL_GRADE_STREAM_ORIGIN_V1.AFTER->SCHOOL_LEVEL_ID AS SCHOOL_LEVEL_ID,
        SCHOOL_LEVEL_GRADE_STREAM_ORIGIN_V1.AFTER->GRADE_ID AS GRADE_ID,
        SCHOOL_LEVEL_GRADE_STREAM_ORIGIN_V1.AFTER->CREATED_AT AS CREATED_AT,
        SCHOOL_LEVEL_GRADE_STREAM_ORIGIN_V1.AFTER->UPDATED_AT AS UPDATED_AT,
        SCHOOL_LEVEL_GRADE_STREAM_ORIGIN_V1.AFTER->DELETED_AT AS DELETED_AT

    FROM SCHOOL_LEVEL_GRADE_STREAM_ORIGIN_V1
    WHERE SCHOOL_LEVEL_GRADE_STREAM_ORIGIN_V1.AFTER->RESOURCE_PATH = '{{ .Values.kecResourcePath }}'
    PARTITION BY SCHOOL_LEVEL_GRADE_STREAM_ORIGIN_V1.AFTER->SCHOOL_LEVEL_ID + SCHOOL_LEVEL_GRADE_STREAM_ORIGIN_V1.AFTER->GRADE_ID
    EMIT CHANGES;
CREATE TABLE IF NOT EXISTS SCHOOL_LEVEL_GRADE_TABLE_FORMATED_V1 (ID VARCHAR PRIMARY KEY) with (kafka_topic='{{ .Values.topicPrefix }}SCHOOL_LEVEL_GRADE_STREAM_FORMATED_V1', value_format='AVRO');


CREATE TABLE IF NOT EXISTS SCHOOL_LEVEL_GRADE_PUBLIC_INFO_V1 
AS SELECT
    AS_VALUE(GRADE_TABLE_FORMATED_V1.ID) AS GRADE_ID,
    GRADE_TABLE_FORMATED_V1.NAME AS NAME,
    GRADE_TABLE_FORMATED_V1.PARTNER_INTERNAL_ID AS PARTNER_INTERNAL_ID,
    GRADE_TABLE_FORMATED_V1.SEQUENCE AS GRADE_SEQUENCE,
    GRADE_TABLE_FORMATED_V1.CREATED_AT AS GRADE_CREATED_AT,
    GRADE_TABLE_FORMATED_V1.UPDATED_AT AS GRADE_UPDATED_AT,
    GRADE_TABLE_FORMATED_V1.DELETED_AT AS GRADE_DELETED_AT,
    GRADE_TABLE_FORMATED_V1.IS_ARCHIVED AS GRADE_IS_ARCHIVED,

    SCHOOL_LEVEL_GRADE_TABLE_FORMATED_V1.ID AS SCHOOL_LEVEL_GRADE_ID,
    SCHOOL_LEVEL_GRADE_TABLE_FORMATED_V1.SCHOOL_LEVEL_ID AS SCHOOL_LEVEL_ID,
    SCHOOL_LEVEL_GRADE_TABLE_FORMATED_V1.CREATED_AT AS SCHOOL_LEVEL_GRADE_CREATED_AT,
    SCHOOL_LEVEL_GRADE_TABLE_FORMATED_V1.UPDATED_AT AS SCHOOL_LEVEL_GRADE_UPDATED_AT,
    SCHOOL_LEVEL_GRADE_TABLE_FORMATED_V1.DELETED_AT AS SCHOOL_LEVEL_GRADE_DELETED_AT

FROM SCHOOL_LEVEL_GRADE_TABLE_FORMATED_V1 
JOIN GRADE_TABLE_FORMATED_V1 ON GRADE_TABLE_FORMATED_V1.ID = SCHOOL_LEVEL_GRADE_TABLE_FORMATED_V1.GRADE_ID;

